def search_city(query: str, limit: int = 10) -> list[dict]:
    """模糊搜索城市经纬度

    Args:
        query: 搜索关键词（如"东莞"、"广东省东莞市"）
        limit: 返回结果数上限

    Returns:
        [{display_name, lon, lat, province}, ...]
    """
    query_lower = query.strip()
    if not query_lower:
        return []

    results = []

    # 精确匹配优先
    if query_lower in CITY_COORDS:
        lon, lat, province = CITY_COORDS[query_lower]
        results.append({
            'display_name': f'{query_lower}（{province}）',
            'lon': lon,
            'lat': lat,
            'province': province,
        })

    # 前缀匹配
    for name, (lon, lat, province) in CITY_COORDS.items():
        if name == query_lower:
            continue  # 已精确匹配
        if name.startswith(query_lower):
            results.append({
                'display_name': f'{name}（{province}）',
                'lon': lon,
                'lat': lat,
                'province': province,
            })

    # 子串匹配
    for name, (lon, lat, province) in CITY_COORDS.items():
        if query_lower in name and name != query_lower and not name.startswith(query_lower):
            results.append({
                'display_name': f'{name}（{province}）',
                'lon': lon,
                'lat': lat,
                'province': province,
            })

    # 去重（按 display_name）
    seen = set()
    unique = []
    for r in results:
        if r['display_name'] not in seen:
            seen.add(r['display_name'])
            unique.append(r)

    return unique[:limit]
